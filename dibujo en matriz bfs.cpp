void imprimir(int (&m)[30][30]){
    for(int i=0;i<30;i++){
        for(int j=0;j<30;j++){
            cout<<(m[i][j]?'X':' ');
        }
        cout<<endl;
    }
    
    //this_thread::sleep_for(3s);
    return;
}

void erosion(int (&m)[30][30]){
    int a[30][30];
    int dx[]={0, 0, 0, 0, 1, 1, -1, -1};
    int dy[]={1, 1, -1, -1, 0, 0, 0, 0};
    for(int i=0;i<30;i++){
        for(int j=0;j<30;j++){
            if(m[i][j]==0){
                a[i][j]=0;
                continue;
            }
            a[i][j]=1;
            for(int h=0; h<8; h++){
                if(i+dx[h]<0 || i+dx[h]>=30 || j+dy[h]<0 || j+dy[h]>=30)continue;
                if(m[i+dx[h]][j+dy[h]]==0)a[i][j]=0;
            }
        }
    }
    
    for(int i=0; i<30; i++){
        for(int j=0; j<30; j++){
            m[i][j]=a[i][j];
        }
    }
    
    return;
}

void menu(int (&m)[30][30]){
    //system("clear");
    
    cout<<"[1] Erosionar\n";
    cout<<"[2] Imprimir\n";
    string ans;
    cin>>ans;
    
    //system("clear");
    
    if(ans=="1" || ans=="2"){
        if(ans=="1")erosion(m);
        if(ans=="2")imprimir(m);
    }
    else return;
    
    return menu(m);
}

